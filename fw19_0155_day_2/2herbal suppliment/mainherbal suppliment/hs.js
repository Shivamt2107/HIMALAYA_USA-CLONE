      if(selected=="Name Ascending"){
        
        data.sort(function(a,b){
        let x=a.title.toUpperCase()
        let y=b.title.toUpperCase()
          if(x>y) return 1
          if(y>x) return -1
          return 0
        })
        
      }
      if(selected=="Name Descending"){
        data.sort(function(a,b){
        let x=a.title.toUpperCase()
        let y=b.title.toUpperCase()
          if(x>y) return -1
          if(y>x) return  1
          return 0
        })
        
        
      }
